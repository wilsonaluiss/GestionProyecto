package validaciones;

import java.util.regex.Matcher;
import java.util.regex.Pattern;

/**
 *
 * @author lexlp
 */
public class Validaciones {

    public static boolean numeroTelefono(String cadena) {
        return cadena.matches("[0-9]*");
    }

    public static boolean validarCorreo(String correo) {
        Pattern pattern = Pattern.compile("(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])");
        Matcher comparador = pattern.matcher(correo);
        return correo.isEmpty() || comparador.find();

    }

    public static boolean contrasena(String contrasena) {
        Pattern pattern = Pattern.compile("^(?=.*[0-9])"
                + "(?=.*[a-z])(?=.*[A-Z])"
                + "(?=.*[@#$%^&+=])"
                + "(?=\\S+$).{8,20}$");
        Matcher comparador = pattern.matcher(contrasena);
        return contrasena.isEmpty() || comparador.find();
    }
    
    public static boolean decimal(String decimal){
            return decimal.matches("^?[0-9]*([\\.][0-9]*)?$");
    }
}
